
public class Rectangle2 {

	public static void main(String[] args) {
		
		//7
		
		for(int a=1;a<=7;a++)
		 {
			 for(int b=1;b<a;b++)
			 {
				 System.out.print(" ");
			 }
			 for(int c=7;c>=a;c--)
			 {
				 System.out.print("*");
			 }
			     System.out.println();
			    
		 }
	}

}
