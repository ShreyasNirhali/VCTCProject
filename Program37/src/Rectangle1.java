
public class Rectangle1 {

	public static void main(String[] args) {
		
		//6
		
		for(int a=1;a<=7;a++)
		 {
			 for(int b=6;b>=a;b--)
			 {
				 System.out.print(" ");
			 }
			 for(int c=1;c<=a;c++)
			 {
				 System.out.print("*");
			 }
			     System.out.println();
			    
		 }
	}

}
