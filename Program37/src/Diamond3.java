
public class Diamond3 {

	public static void main(String[] args) {
		
		for(int a=1;a<=7;a++)
		 {
			 
			 for(int c=1;c<=a;c++)
			 {
				 System.out.print("*");
			 }
			     System.out.println();
		 }
		
		for(int a=1;a<=6;a++)
		 {
			 
			 for(int c=6;c>=a;c--)
			 {
				 System.out.print("*");
			 }
			     System.out.println();
		 }
	}

}
